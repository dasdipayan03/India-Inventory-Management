// db.js
const { Pool } = require("pg");

// ‚úÖ Log the env check early
console.log("üîç Loaded DATABASE_URL:", process.env.DATABASE_URL ? "‚úÖ Exists" : "‚ùå Missing");

if (!process.env.DATABASE_URL) {
  console.error("üö® ERROR: DATABASE_URL is not defined in environment!");
  process.exit(1);
}

const pool = new Pool({
  connectionString: process.env.DATABASE_URL,
  ssl: {
    require: true,
    rejectUnauthorized: false, // Required for Render SSL
  },
});

pool.connect()
  .then(() => console.log("‚úÖ PostgreSQL connected successfully"))
  .catch((err) => {
    console.error("‚ùå Database connection error:", err.message);
    process.exit(1);
  });

module.exports = pool;
