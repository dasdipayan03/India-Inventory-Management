// db.js
const { Pool } = require("pg");

console.log("üîç Loaded DATABASE_URL:", process.env.DATABASE_URL ? "‚úÖ Exists" : "‚ùå Missing");

if (!process.env.DATABASE_URL) {
  console.error("üö® DATABASE_URL missing! Exiting...");
  process.exit(1);
}

const pool = new Pool({
  connectionString: process.env.DATABASE_URL,
  ssl: {
    require: true,
    rejectUnauthorized: false,
  },
});

pool.connect()
  .then(() => console.log("‚úÖ PostgreSQL connected successfully"))
  .catch((err) => {
    console.error("‚ùå Database connection error:", err.stack);
    process.exit(1);
  });

module.exports = pool;
